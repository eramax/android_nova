AOSP_GRAPHICS = ROOT / "frameworks/base/graphics/java"
AOSP_CORE_BP = ROOT / "frameworks/base/core/java/Android.bp"
AOSP_GRAPHICS_BP = ROOT / "frameworks/base/graphics/java/Android.bp"
NOVA_HYBRID_BEGIN = "// NOVA_HYBRID_FILEGROUP_BEGIN"
NOVA_HYBRID_END = "// NOVA_HYBRID_FILEGROUP_END"

