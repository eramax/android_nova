def load_bridge_files() -> set[str]:
    keep: set[str] = set()
    for line in BRIDGE_LIST.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        keep.add(line)
    return keep


def glob_aosp_sources() -> list[str]:
    import glob

    paths: list[str] = []
    for _name, base, patterns in AOSP_PACKAGES:
        if not base.is_dir():
            continue
        for pattern in patterns:
            for path in sorted(base.glob(pattern)):
                if path.suffix != ".java":
                    continue
                rel = path.relative_to(base).as_posix()
                paths.append(rel)
    return sorted(set(paths))


